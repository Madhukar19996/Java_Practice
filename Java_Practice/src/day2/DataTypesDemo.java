package day2;

public class DataTypesDemo {

	public static void main(String[] args) 
	
	{
		// Numeric Data Type
		
		int a=100, b=300;
		
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(a+b);
		
		byte by=110;
		System.out.println(by);
		
		short sh=250;
		System.out.println(sh);
		
		long l=1234567898;
		
		System.out.println(l);
		
		
		// Decimal Numbers - float , double 
		
		
		float price=150.5f;   // Literal is needed at the end of the value 
		System.out.println(price);
		
		
		double items_price=153.2323431223;
		System.out.println(items_price);
		
		// Character
		
		char grad='A';
		System.out.println("The grad of the student is :"+grad);
		
		
		// Boolean -- It allowed only true/False 
		 boolean bool=true;
		 System.out.println(bool);
		 
		 
	  // String  -- It is non-primitive type data type 
		 String name="Madhukar";
		 System.out.println(name);
			
			
		
		
				
		
		
		
		
		
		
		
		

	}

}
