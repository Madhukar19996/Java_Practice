package day3;

public class IncrementOperators {

	public static void main(String[] args) 
	
	{
		// Increment & Decrement operator ++  --
		
		int a =10;
		 //a=a+1;
		 a++;
		 System.out.println(a);
		 
		 
		
		 //post increment 
		 int x=10;
		 int res=x++;
		 System.out.println(res); // 10
		 
		 
		 //pre increment 
		 int x1=10;
		 int res1=++x1;
		 System.out.println(res1); // 11
		
		

	}

}
